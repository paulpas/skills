@dataclass
class TokenMetrics:
    """Token usage and cost metrics for LLM calls."""

    prompt_tokens_with_router: int = 0
    response_tokens_with_router: int = 0
    prompt_tokens_without_router: int = 0
    response_tokens_without_router: int = 0
    total_tokens_with_router: int = 0
    total_tokens_without_router: int = 0
    token_efficiency_ratio: float = 0.0  # output/input
    estimated_cost_with_router_usd: float = 0.0
    estimated_cost_without_router_usd: float = 0.0
    token_savings_pct: float = 0.0

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary."""
        return asdict(self)


@dataclass
class CodeQualityMetrics:
    """Code quality assessment results."""

    code_correctness_pct: float  # % test cases passing
    compiles: bool  # Does code parse without syntax errors
    cyclomatic_complexity: int  # Cyclomatic complexity score
    has_error_handling: bool  # Proper try/catch or error returns
    execution_time_ms: float  # Execution time for test cases
    test_cases_passed: int  # Number of passing test cases
    test_cases_total: int  # Total test cases
    sloc: int  # Source lines of code (excluding comments/blanks)
    maintainability_score: float  # 0-100, based on complexity and length

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary."""
        return asdict(self)


class LLMCodeGenerator(ABC):
    """Abstract base class for LLM code generators."""

    @abstractmethod
    def generate_code(self, prompt: str, language: str) -> str:
        """Generate code for a given prompt and language."""
        pass


class RealLLMCodeGenerator(LLMCodeGenerator):
    """Real LLM code generator using actual API calls via LLMFactory."""

    def __init__(self, llm_client):
        """Initialize with an LLM client from LLMFactory."""
        self.client = llm_client

    def generate_code(self, prompt: str, language: str) -> str:
        """Generate code using real LLM API."""
        formatted_prompt = f"""Generate {language} code to solve this:

{prompt}

Requirements:
- Must be syntactically correct and executable
- Include error handling
- Add meaningful comments
- Follow best practices for {language}
- Return clean, production-ready code
- Code only, no explanation"""

        try:
            response = self.client.generate(
                formatted_prompt,
                temperature=0.2,  # Low temperature for code generation
                max_tokens=2048,
            )

            code = response.text

            # Extract code from markdown if present
            if f"```{language}" in code:
                code = code.split(f"```{language}")[1].split("```")[0].strip()
            elif "```" in code:
                code = code.split("```")[1].split("```")[0].strip()

            return code

        except Exception as e:
            raise RuntimeError(f"LLM code generation failed: {e}")


