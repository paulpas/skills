    """OpenAI-based code generator."""

    def __init__(self, api_key: str, model: str = "gpt-4"):
        if openai is None:
            raise ImportError("openai package required for OpenAI backend")

        self.client = openai.OpenAI(api_key=api_key)
        self.model = model

