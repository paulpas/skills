class OllamaCodeGenerator(LLMCodeGenerator):
    """Local Ollama-based code generator (fallback/testing)."""

    def __init__(
        self, base_url: str = "http://localhost:11434", model: str = "codellama"
    ):
        self.base_url = base_url
        self.model = model

    def generate_code(self, prompt: str, language: str) -> str:
        """Generate code using local Ollama instance."""
        import requests

        formatted_prompt = f"""Generate {language} code to solve this:

{prompt}

Requirements:
- Include error handling
- Add meaningful comments
- Return clean, production-ready code
- No explanations, just code"""

        try:
            response = requests.post(
                f"{self.base_url}/api/generate",
                json={"model": self.model, "prompt": formatted_prompt, "stream": False},
                timeout=30,
            )
            response.raise_for_status()
            code = response.json().get("response", "")

            # Extract code from markdown if present
            if f"```{language}" in code:
                code = code.split(f"```{language}")[1].split("```")[0].strip()
            elif "```" in code:
                code = code.split("```")[1].split("```")[0].strip()

            return code
        except Exception as e:
            raise RuntimeError(f"Failed to connect to Ollama: {e}")


class CodeQualityAnalyzer:
    """Analyzes generated code quality using static analysis and execution."""

    # Token pricing configuration (USD per token)
    PRICING_CONFIG = {
        "gpt-4": {
            "input": 0.03 / 1000,  # $0.03 per 1K input tokens
            "output": 0.06 / 1000,  # $0.06 per 1K output tokens
        },
        "gpt-3.5-turbo": {
            "input": 0.0005 / 1000,  # $0.0005 per 1K input tokens
            "output": 0.0015 / 1000,  # $0.0015 per 1K output tokens
        },
        "claude-3-opus": {
            "input": 0.015 / 1000,  # $0.015 per 1K input tokens
            "output": 0.075 / 1000,  # $0.075 per 1K output tokens
        },
        "claude-3-sonnet": {
            "input": 0.003 / 1000,  # $0.003 per 1K input tokens
            "output": 0.015 / 1000,  # $0.015 per 1K output tokens
        },
        "ollama": {
            "input": 0.0,
            "output": 0.0,
        },
    }

    def __init__(self, timeout: int = 30):
        """Initialize analyzer.

        Args:
            timeout: Timeout in seconds for code execution
        """
        self.timeout = timeout
        self.token_history: List[Dict] = []
        self.total_tokens_with_router = 0
        self.total_tokens_without_router = 0

    def analyze_python(
        self,
        code: str,
        test_cases: List[Dict[str, Any]],
    ) -> CodeQualityMetrics:
        """Analyze Python code quality.

        Args:
            code: Python source code
            test_cases: List of {"input": ..., "expected_output": ...} dicts

        Returns:
            CodeQualityMetrics instance
        """
        metrics = CodeQualityMetrics(
            code_correctness_pct=0.0,
            compiles=False,
            cyclomatic_complexity=0,
            has_error_handling=False,
            execution_time_ms=0.0,
            test_cases_passed=0,
            test_cases_total=len(test_cases),
            sloc=0,
            maintainability_score=0.0,
        )

        # Check if code compiles
        try:
            tree = ast.parse(code)
            metrics.compiles = True
        except SyntaxError:
            return metrics

        # Calculate SLOC (source lines of code)
        metrics.sloc = self._count_sloc(code)

        # Calculate cyclomatic complexity
        metrics.cyclomatic_complexity = self._calculate_cyclomatic_complexity(tree)

        # Check for error handling
        metrics.has_error_handling = self._has_error_handling(tree)

        # Run test cases
        try:
            start_time = time.time()
            passed, total = self._run_python_tests(code, test_cases)
            elapsed_ms = (time.time() - start_time) * 1000

            metrics.test_cases_passed = passed
            metrics.test_cases_total = total
            metrics.execution_time_ms = elapsed_ms

            if total > 0:
                metrics.code_correctness_pct = (passed / total) * 100.0
        except Exception:
            # If test execution fails, mark as failure but don't crash
            metrics.code_correctness_pct = 0.0

        # Calculate maintainability score (0-100)
        metrics.maintainability_score = self._calculate_maintainability(metrics)

        return metrics

    def analyze_javascript(
        self,
        code: str,
        test_cases: List[Dict[str, Any]],
    ) -> CodeQualityMetrics:
        """Analyze JavaScript code quality."""
        metrics = CodeQualityMetrics(
            code_correctness_pct=0.0,
            compiles=False,
            cyclomatic_complexity=0,
            has_error_handling=False,
            execution_time_ms=0.0,
            test_cases_passed=0,
            test_cases_total=len(test_cases),
            sloc=0,
            maintainability_score=0.0,
        )

        # Check if code compiles (basic syntax check)
        try:
            result = subprocess.run(
                ["node", "-c"],  # Check syntax
                input=code.encode(),
                capture_output=True,
                timeout=5,
            )
            metrics.compiles = result.returncode == 0
        except (FileNotFoundError, subprocess.TimeoutExpired):
            # Node not available, skip compile check
            metrics.compiles = True

        if not metrics.compiles:
            return metrics

        # Calculate SLOC
        metrics.sloc = self._count_sloc(code)

        # Check for error handling (try/catch or error callbacks)
        metrics.has_error_handling = (
            "try" in code or "catch" in code or ".catch" in code
        )

        # Estimate cyclomatic complexity from if/switch statements
        metrics.cyclomatic_complexity = self._estimate_js_complexity(code)

        # Try to run tests
        try:
            start_time = time.time()
            passed, total = self._run_javascript_tests(code, test_cases)
            elapsed_ms = (time.time() - start_time) * 1000

            metrics.test_cases_passed = passed
            metrics.test_cases_total = total
            metrics.execution_time_ms = elapsed_ms

            if total > 0:
                metrics.code_correctness_pct = (passed / total) * 100.0
        except Exception:
            metrics.code_correctness_pct = 0.0

        metrics.maintainability_score = self._calculate_maintainability(metrics)

        return metrics

    def analyze_go(
        self,
        code: str,
        test_cases: List[Dict[str, Any]],
    ) -> CodeQualityMetrics:
        """Analyze Go code quality."""
        metrics = CodeQualityMetrics(
            code_correctness_pct=0.0,
            compiles=False,
            cyclomatic_complexity=0,
            has_error_handling=False,
            execution_time_ms=0.0,
            test_cases_passed=0,
            test_cases_total=len(test_cases),
            sloc=0,
            maintainability_score=0.0,
        )

        # Check syntax
        try:
            result = subprocess.run(
                ["go", "fmt"],
                input=code.encode(),
                capture_output=True,
                timeout=5,
            )
            metrics.compiles = result.returncode == 0
        except FileNotFoundError:
            metrics.compiles = True

        if not metrics.compiles:
            return metrics

        metrics.sloc = self._count_sloc(code)
        metrics.has_error_handling = "err != nil" in code or "if err" in code
        metrics.cyclomatic_complexity = self._estimate_go_complexity(code)
        metrics.maintainability_score = self._calculate_maintainability(metrics)

        return metrics

    def analyze_rust(
        self,
        code: str,
        test_cases: List[Dict[str, Any]],
    ) -> CodeQualityMetrics:
        """Analyze Rust code quality."""
        metrics = CodeQualityMetrics(
            code_correctness_pct=0.0,
            compiles=False,
            cyclomatic_complexity=0,
            has_error_handling=False,
            execution_time_ms=0.0,
            test_cases_passed=0,
            test_cases_total=len(test_cases),
            sloc=0,
            maintainability_score=0.0,
        )

        # Check if code compiles
        try:
            result = subprocess.run(
                ["rustc", "--crate-type", "lib", "-"],
                input=code.encode(),
                capture_output=True,
                timeout=10,
            )
            metrics.compiles = result.returncode == 0
        except FileNotFoundError:
            metrics.compiles = True

        metrics.sloc = self._count_sloc(code)
        metrics.has_error_handling = "?" in code or "Result" in code or "Option" in code
        metrics.cyclomatic_complexity = self._estimate_rust_complexity(code)
        metrics.maintainability_score = self._calculate_maintainability(metrics)

        return metrics

    @staticmethod
    def _count_sloc(code: str) -> int:
        """Count source lines of code (excluding comments and blanks)."""
        lines = code.split("\n")
        sloc = 0
        in_multiline_comment = False

        for line in lines:
            stripped = line.strip()

            # Skip empty lines
            if not stripped:
                continue

            # Handle Python/JS multiline comments
            if '"""' in stripped or "'''" in stripped:
                in_multiline_comment = not in_multiline_comment
                continue

            if in_multiline_comment:
                continue

            # Skip single-line comments
            if stripped.startswith("#") or stripped.startswith("//"):
                continue

            sloc += 1

        return sloc

    @staticmethod
    def _calculate_cyclomatic_complexity(tree: ast.AST) -> int:
        """Calculate cyclomatic complexity from AST."""
        complexity = 1

        for node in ast.walk(tree):
            if isinstance(node, (ast.If, ast.For, ast.While)):
                complexity += 1
            elif isinstance(node, ast.ExceptHandler):
                complexity += 1
            elif isinstance(node, (ast.BoolOp,)):
                # Count 'and'/'or' operators
                complexity += len(node.values) - 1

        return complexity

    @staticmethod
    def _has_error_handling(tree: ast.AST) -> bool:
        """Check if code has proper error handling."""
        for node in ast.walk(tree):
            if isinstance(node, ast.Try):
                return True

        return False

    @staticmethod
    def _estimate_js_complexity(code: str) -> int:
        """Estimate cyclomatic complexity for JavaScript."""
        complexity = 1
        complexity += code.count("if (") + code.count("if(")
        complexity += code.count("for (") + code.count("for(")
        complexity += code.count("while (") + code.count("while(")
        complexity += code.count("? ") + code.count("?")  # ternary
        complexity += code.count(" && ") - 1
        complexity += code.count(" || ") - 1

        return max(1, complexity)

    @staticmethod
    def _estimate_go_complexity(code: str) -> int:
        """Estimate cyclomatic complexity for Go."""
        complexity = 1
        complexity += code.count("if ") + code.count("if(")
        complexity += code.count("for ")
        complexity += code.count("switch ")
        complexity += code.count("case ")
        complexity += code.count(" && ") - 1
        complexity += code.count(" || ") - 1

        return max(1, complexity)

    @staticmethod
    def _estimate_rust_complexity(code: str) -> int:
        """Estimate cyclomatic complexity for Rust."""
        complexity = 1
        complexity += code.count("if ")
        complexity += code.count("for ")
        complexity += code.count("match ")
        complexity += code.count("=>")
        complexity += code.count(" && ") - 1
        complexity += code.count(" || ") - 1

        return max(1, complexity)

    @staticmethod
    def _calculate_maintainability(metrics: CodeQualityMetrics) -> float:
        """Calculate maintainability score (0-100)."""
        score = 100.0

        # Penalize high complexity
        if metrics.cyclomatic_complexity > 10:
            score -= (metrics.cyclomatic_complexity - 10) * 2

        # Penalize long functions
        if metrics.sloc > 100:
            score -= (metrics.sloc - 100) / 10

        # Penalize missing error handling
        if not metrics.has_error_handling:
            score -= 15

        # Penalize test failures
        if metrics.test_cases_total > 0:
            failure_rate = (
                metrics.test_cases_total - metrics.test_cases_passed
            ) / metrics.test_cases_total
            score -= failure_rate * 30

        return max(0.0, min(100.0, score))

    def _count_tokens(self, text: str, model: str) -> int:
        """Count tokens in text using appropriate method for model.

        Args:
            text: Text to count tokens for
            model: Model name (gpt-4, claude-opus, etc.)

        Returns:
            Estimated token count
        """
        if not text:
            return 0

        # Try using tiktoken for OpenAI models
        if model.startswith("gpt") and tiktoken is not None:
            try:
                encoding = tiktoken.encoding_for_model(model)
                return len(encoding.encode(text))
            except Exception:
                pass

        # Fallback: estimate tokens (rough approximation)
        # ~4 characters per token on average
        return max(1, len(text) // 4)

    def _calculate_cost(self, tokens_in: int, tokens_out: int, model: str) -> float:
        """Calculate estimated cost for LLM tokens.

        Args:
            tokens_in: Number of input tokens
            tokens_out: Number of output tokens
            model: Model name

        Returns:
            Estimated cost in USD
        """
        pricing = self.PRICING_CONFIG.get(model, self.PRICING_CONFIG["gpt-4"])
        input_cost = tokens_in * pricing["input"]
        output_cost = tokens_out * pricing["output"]
        return input_cost + output_cost

    def track_llm_call(
        self,
        prompt: str,
        response: str,
        model: str,
        with_router: bool = True,
    ) -> TokenMetrics:
        """Track token usage for an LLM call.

        Args:
            prompt: Prompt sent to LLM
            response: Response from LLM
            model: Model name
            with_router: Whether router context was included

        Returns:
            TokenMetrics for this call
        """
        prompt_tokens = self._count_tokens(prompt, model)
        response_tokens = self._count_tokens(response, model)
        total_tokens = prompt_tokens + response_tokens

        # Update running totals
        if with_router:
            self.total_tokens_with_router += total_tokens
        else:
            self.total_tokens_without_router += total_tokens

        # Calculate metrics
        efficiency = response_tokens / prompt_tokens if prompt_tokens > 0 else 0.0
        cost_with = self._calculate_cost(prompt_tokens, response_tokens, model)
        cost_without = 0.0  # Placeholder for comparison

        metrics = TokenMetrics(
            prompt_tokens_with_router=prompt_tokens if with_router else 0,
            response_tokens_with_router=response_tokens if with_router else 0,
            prompt_tokens_without_router=prompt_tokens if not with_router else 0,
            response_tokens_without_router=response_tokens if not with_router else 0,
            total_tokens_with_router=total_tokens if with_router else 0,
            total_tokens_without_router=total_tokens if not with_router else 0,
            token_efficiency_ratio=efficiency,
            estimated_cost_with_router_usd=cost_with if with_router else 0.0,
            estimated_cost_without_router_usd=cost_without,
        )

        # Store in history
        self.token_history.append(
            {
                "model": model,
                "with_router": with_router,
                "tokens": total_tokens,
                "cost_usd": cost_with if with_router else 0.0,
                "timestamp": time.time(),
            }
        )

        return metrics

    def get_token_report(self) -> Dict[str, Any]:
        """Generate comprehensive token usage report.

        Returns:
            Dictionary with token statistics
        """
        if not self.token_history:
            return {
                "total_calls": 0,
                "total_tokens": 0,
                "total_cost_usd": 0.0,
            }

        total_cost = sum(entry["cost_usd"] for entry in self.token_history)
        total_tokens = sum(entry["tokens"] for entry in self.token_history)

        # Group by model
        by_model: Dict[str, List] = {}
        for entry in self.token_history:
            model = entry["model"]
            if model not in by_model:
                by_model[model] = []
            by_model[model].append(entry)

        model_stats = {}
        for model, entries in by_model.items():
            model_stats[model] = {
                "calls": len(entries),
                "total_tokens": sum(e["tokens"] for e in entries),
                "total_cost_usd": sum(e["cost_usd"] for e in entries),
                "avg_tokens_per_call": sum(e["tokens"] for e in entries) / len(entries),
            }

        return {
            "total_calls": len(self.token_history),
            "total_tokens_with_router": self.total_tokens_with_router,
            "total_tokens_without_router": self.total_tokens_without_router,
            "total_tokens": total_tokens,
            "total_cost_usd": round(total_cost, 4),
            "estimated_monthly_cost_usd": round(total_cost * 30, 2),
            "estimated_annual_cost_usd": round(total_cost * 365, 2),
            "by_model": model_stats,
            "token_savings_pct": (
                self._calculate_token_savings_pct()
                if self.total_tokens_without_router > 0
                else 0.0
            ),
        }

    def _calculate_token_savings_pct(self) -> float:
        """Calculate token savings percentage from router."""
        if self.total_tokens_without_router == 0:
            return 0.0
        savings = self.total_tokens_without_router - self.total_tokens_with_router
        return (savings / self.total_tokens_without_router) * 100

    def _run_python_tests(self, code: str, test_cases: List[Dict]) -> Tuple[int, int]:
        """Run Python test cases and return (passed, total)."""
        if not test_cases:
            return 0, 0

        # Create test script
        test_script = f"""
import sys
import json

{code}

test_cases = {json.dumps(test_cases)}
passed = 0
total = len(test_cases)

for i, test in enumerate(test_cases):
    try:
        # Try to call the main function with test inputs
        if 'input' in test:
            if isinstance(test['input'], list):
                result = main(*test['input'])
            else:
                result = main(test['input'])
        else:
            result = main()
        
        expected = test.get('expected_output', test.get('expected', None))
        if result == expected:
            passed += 1
    except Exception as e:
        pass

print(json.dumps({{"passed": passed, "total": total}}))
"""

        try:
            result = subprocess.run(
                ["python3", "-c", test_script],
                capture_output=True,
                timeout=self.timeout,
                text=True,
            )

            if result.returncode != 0:
                return 0, len(test_cases)

            output = result.stdout.strip()
            data = json.loads(output)
            return data["passed"], data["total"]
        except Exception:
            return 0, len(test_cases)

    def _run_javascript_tests(
        self, code: str, test_cases: List[Dict]
    ) -> Tuple[int, int]:
        """Run JavaScript test cases."""
        if not test_cases:
            return 0, 0

        test_script = f"""
{code}

const testCases = {json.dumps(test_cases)};
let passed = 0;
const total = testCases.length;

for (const test of testCases) {{
    try {{
        let result;
        if (test.input !== undefined) {{
            if (Array.isArray(test.input)) {{
                result = main(...test.input);
            }} else {{
                result = main(test.input);
            }}
        }} else {{
            result = main();
        }}
        
        const expected = test.expected_output !== undefined ? test.expected_output : test.expected;
        if (JSON.stringify(result) === JSON.stringify(expected)) {{
            passed++;
        }}
    }} catch (e) {{
        // Test failed
    }}
}}

console.log(JSON.stringify({{passed, total}}));
"""

        try:
            result = subprocess.run(
                ["node", "-e", test_script],
                capture_output=True,
                timeout=self.timeout,
                text=True,
            )

            if result.returncode != 0:
                return 0, len(test_cases)

            output = result.stdout.strip()
            data = json.loads(output)
            return data["passed"], data["total"]
        except Exception:
            return 0, len(test_cases)


class LLMPerformanceBenchmark:
    """Benchmark LLM performance on coding tasks with REAL API calls."""

    def __init__(self, llm_model: str = "gpt-4", api_key: Optional[str] = None):
        """Initialize benchmark with real LLM client.

        Args:
            llm_model: LLM model to use (gpt-4, claude-opus, etc.)
            api_key: Optional API key (uses env vars if not provided)
        """
        self.llm_model = llm_model
        self.api_key = api_key
        self.generator = self._create_generator()
        self.analyzer = CodeQualityAnalyzer()

    def _create_generator(self) -> Optional[LLMCodeGenerator]:
        """Create real code generator using actual API calls.

        Falls through to mock mode (returns None) when:
        - The model is not in the registry (ValueError)
        - The required provider package is not installed (ImportError)
        - The API key / server is not configured (ValueError from factory)

        In mock mode, evaluate_exercise() returns synthetic scores so the
        benchmark harness can still run for structure/routing tests.
        """
        try:
            from llm_factory import LLMFactory

            # Create real LLM client — raises ValueError for unsupported models
            llm_client = LLMFactory.create(self.llm_model)
            return RealLLMCodeGenerator(llm_client)
        except (ImportError, ValueError) as e:
            print(
                f"⚠️  Could not create real LLM client for '{self.llm_model}': {e}\n"
                f"   Falling back to mock mode (no real API calls will be made)."
            )
            return None

    def evaluate_exercise(
        self,
        exercise: Dict[str, Any],
        with_router_skills: Optional[List[str]] = None,
    ) -> Dict[str, Any]:
        """Evaluate LLM performance on a single exercise.

        Args:
            exercise: Exercise dictionary with coding_challenge
            with_router_skills: Skills loaded by router (for context)

        Returns:
            Result dictionary with code quality metrics
        """
        if self.generator is None:
            return self._mock_result(exercise, with_router_skills)

        coding_challenge = exercise.get("coding_challenge")
        if not coding_challenge:
            return {"error": "No coding_challenge in exercise"}

        try:
            # Generate code
            prompt = coding_challenge["description"]
            language = coding_challenge.get("language", "python")
            code = self.generator.generate_code(prompt, language)

            # Analyze code quality
            test_cases = coding_challenge.get("success_criteria", {}).get(
                "test_cases", []
            )

            if language == "python":
                metrics = self.analyzer.analyze_python(code, test_cases)
            elif language == "javascript":
                metrics = self.analyzer.analyze_javascript(code, test_cases)
            elif language == "go":
                metrics = self.analyzer.analyze_go(code, test_cases)
            elif language == "rust":
                metrics = self.analyzer.analyze_rust(code, test_cases)
            else:
                metrics = CodeQualityMetrics(
                    code_correctness_pct=0.0,
                    compiles=False,
                    cyclomatic_complexity=0,
                    has_error_handling=False,
                    execution_time_ms=0.0,
                    test_cases_passed=0,
                    test_cases_total=len(test_cases),
                    sloc=0,
                    maintainability_score=0.0,
                )

            return {
                "exercise_name": exercise.get("name"),
                "tier": exercise.get("tier"),
                "language": language,
                "code_generated": code[:500],  # First 500 chars
                "router_skills_loaded": with_router_skills or [],
                "metrics": metrics.to_dict(),
            }

        except Exception as e:
            return {
                "exercise_name": exercise.get("name"),
                "error": str(e),
            }

    @staticmethod
    def _mock_result(
        exercise: Dict[str, Any],
        with_router_skills: Optional[List[str]] = None,
    ) -> Dict[str, Any]:
        """Generate mock result (for testing without LLM API)."""
        coding_challenge = exercise.get("coding_challenge", {})
        difficulty = coding_challenge.get("difficulty", "medium")

        # Mock scores based on difficulty
        if difficulty == "easy":
            correctness = 95.0
            complexity = 2
            sloc = 15
        elif difficulty == "hard":
            correctness = 75.0
            complexity = 8
            sloc = 80
        else:
            correctness = 85.0
            complexity = 5
            sloc = 40

        test_cases = coding_challenge.get("success_criteria", {}).get("test_cases", [])
        total_tests = len(test_cases)
        passed_tests = int((correctness / 100.0) * total_tests)

        # Slightly improve with router skills
        if with_router_skills:
            correctness = min(98.0, correctness + 5.0)
            complexity = max(1, complexity - 1)

        return {
            "exercise_name": exercise.get("name"),
            "tier": exercise.get("tier"),
            "language": coding_challenge.get("language", "python"),
            "code_generated": "# Mock generated code (no API key)",
            "router_skills_loaded": with_router_skills or [],
            "metrics": {
                "code_correctness_pct": correctness,
                "compiles": True,
                "cyclomatic_complexity": complexity,
                "has_error_handling": True,
                "execution_time_ms": 125.0 if difficulty != "hard" else 250.0,
                "test_cases_passed": passed_tests,
                "test_cases_total": total_tests,
                "sloc": sloc,
                "maintainability_score": 100.0 - (complexity * 3) - (sloc / 10.0),
            },
        }


def create_llm_benchmark(
    llm_model: str = "gpt-4",
    api_key: Optional[str] = None,
) -> LLMPerformanceBenchmark:
    """Factory function to create LLM benchmark instance."""
    return LLMPerformanceBenchmark(llm_model, api_key)
